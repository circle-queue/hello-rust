def roll_up(input: list[str]) -> list[str]:
    line_len = len(input[0])
    out = [list(line) for line in input]
    for j in range(line_len):
        roll_to = None
        # print(column)
        for i in range(len(input)):
            char = input[i][j]
            match char:
                case "#":
                    roll_to = i + 1
                case "O":
                    if roll_to is None:
                        continue
                    out[i][j], out[roll_to][j] = out[roll_to][j], out[i][j]
                    roll_to += 1
                case ".":
                    if roll_to is None:
                        roll_to = i
                    pass
                case _:
                    raise RuntimeError()
        # print(column)
        # print()
    out = ["".join(line) for line in out]
    return out


def rotate_right(input: list[str]) -> list[str]:
    output = []
    for j in range(len(input[0])):
        column = "".join([input[i][j] for i in range(len(input[0]))])
        output.append(column[::-1])
    return output


def calc_top_weight(input: list[str]) -> int:
    total_weight = 0
    for col in rotate_right(input):
        for weight, char in enumerate(col, start=1):
            if char == "O":
                total_weight += weight
    return total_weight


def solve1(input: list[str]) -> str:
    input = roll_up(input)
    return str(calc_top_weight(input))


def solve2(input: list[str]) -> str:
    seen_id = {}
    todo = 1_000_000_000
    cycle_id = 0
    while todo:
        for _ in range(4):
            input = roll_up(input)
            input = rotate_right(input)

        cycle_id += 1
        todo -= 1
        # print(cycle)
        # print(grid_id)
        # print()

        grid_id = "\n".join(input)
        if grid_id in seen_id:
            loop_len = cycle_id - seen_id[grid_id]
            # print(f"{cycle_id=} {seen_id[grid_id]=} {todo=} {loop_len=}")
            todo %= loop_len
            # print(f"{todo=}")
        seen_id[grid_id] = cycle_id

    return str(calc_top_weight(input))


