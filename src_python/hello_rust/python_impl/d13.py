def is_symmetric(group_: str, i: int, errors_allowed: int):
    i_1, i_2 = i, i + 1
    while i_1 >= 0 and i_2 < len(group_):
        # print(f"Checking {i=} {i_1=} {i_2=}")
        for char1, char2 in zip(group_[i_1], group_[i_2]):
            if char1 != char2:
                if not errors_allowed:
                    return False
                errors_allowed -= 1
        i_1 -= 1
        i_2 += 1
    return errors_allowed == 0  # Exactly errors_allowed errors allowed


def solve(input: list[str], errors_allowed: int) -> str:
    groups = [[]]
    for line in input:
        if not line:
            groups.append([])
        else:
            groups[-1].append(line)

    result = 0
    # sols = {}
    for group in groups:
        group_T = [
            "".join(group[i][j] for i in range(len(group)))
            for j in range(len(group[0]))
        ]
        # g_id = "\n".join(group)
        for group_, scale in [(group, 100), (group_T, 1)]:
            # print("\n".join(group))
            for i in range(len(group_) - 1):
                if is_symmetric(group_, i, errors_allowed):
                    # print(f"Found symmetric at {i} @{scale}")
                    result += (1 + i) * scale
                    # if g_id in sols:
                    #     raise Exception(f"Already found at {sols[i]}")
                    # sols[g_id] = i
        # if g_id not in sols:
        #     raise Exception(f"Could not find solution for:\n{g_id}")

    return str(result)


def solve1(input: list[str]) -> str:
    return solve(input, 0)


